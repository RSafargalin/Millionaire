import UIKit

extension UIImage {
    static var addConcreteTaskImage: UIImage? {
        return UIImage(systemName: "plus.square")
    }
    
    static var addCompositeTaskImage: UIImage? {
        return UIImage(systemName: "plus.square.on.square")
    }
}
